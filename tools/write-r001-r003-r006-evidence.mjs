import { readFile, writeFile } from 'node:fs/promises';
import { basename, join } from 'node:path';

const [directory, overallRaw] = process.argv.slice(2);
if (!directory) throw new Error('evidence directory is required');
const overall = Number(overallRaw ?? 1);
const checks = [
  'architecture-inventory',
  'architecture-boundaries',
  'r1-closure',
  'r2-closure',
  'r3-closure',
  'r6-closure',
  'lint',
  'typecheck',
];

const rows = [];
const details = [];
for (const check of checks) {
  const code = Number((await readFile(join(directory, `${check}.code`), 'utf8')).trim());
  const log = (await readFile(join(directory, `${check}.log`), 'utf8')).trim();
  rows.push(`| \`${check}\` | ${code === 0 ? '✅ PASS' : `❌ FAIL (${code})`} |`);
  details.push(`## ${check}\n\n\`\`\`text\n${log.slice(-8000) || '(no output)'}\n\`\`\``);
}

const content = `# R-001, R-002, R-003 and R-006 CI Closure Evidence

Status: **${overall === 0 ? 'PASS' : 'FAIL'}**

Generated by: \`.github/workflows/verify-r001-r003-r006-closure.yml\`  
Commit under verification: \`${process.env.GITHUB_SHA ?? 'unknown'}\`  
Workflow run: ${process.env.GITHUB_SERVER_URL ?? 'https://github.com'}/${process.env.GITHUB_REPOSITORY ?? 'unknown'}/actions/runs/${process.env.GITHUB_RUN_ID ?? 'unknown'}

| Check | Result |
|---|---|
${rows.join('\n')}

${details.join('\n\n')}
`;

await writeFile(join(process.cwd(), 'docs', 'architecture', 'r001-r003-r006-ci-evidence.md'), content, 'utf8');
console.log(`Wrote ${basename('r001-r003-r006-ci-evidence.md')} with status ${overall === 0 ? 'PASS' : 'FAIL'}`);
